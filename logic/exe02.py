# Calculator

numberOne = int(input("Digite o primeiro número: "))
numberTwo = int(input("Digite o segundo número: "))

sum = numberOne + numberTwo
subtraction = numberOne - numberTwo
division = numberOne / numberTwo
multiplication = numberOne * numberTwo

print("A soma total dos valores é: ", sum)
print("A subtração dessa conta é: ", subtraction)
print("A divisão dessa conta é: ", division)
print("A multiplicação dessa conta é: ", multiplication)

