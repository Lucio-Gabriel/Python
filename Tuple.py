filmsTuple = ("Batman", "Superman", "Baby Driver",
             "Esquadrão suicida", "Coringa", "Piratas do caribe")

print(type(filmsTuple))

# 1 - Busca os dois primeiros itens da tupla
print(filmsTuple[:2])

# 2 - Busca o ultimo item da tupla
print(filmsTuple[-1])

# 3 - Buscar filmes até uma determinada posição
print(filmsTuple[:3])

# 4 - Buscar filmes de uma posição em diante
print(filmsTuple[2:])